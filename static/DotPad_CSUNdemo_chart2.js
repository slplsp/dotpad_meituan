async function onS4ButtonClick() {
    try {
        // 从后端获取二维数组
        const response = await fetch('/get-array/'); // 确保路由路径正确
        if (!response.ok) {
            throw new Error(`HTTP error! status: ${response.status}`);
        }

        const data = await response.json(); // 获取 JSON 数据
        const array2D = data.array; // 提取数组

        // 将获取的二维数组转换为适合发送的数据格式
        const F1 = [];
        F1.push("0000000000000000000000000000000000000000" + trans_hex_pad(array2D));

        // 调用 DotPad 的 send 方法发送数据
        myDotPad.send(F1[0]);

        console.log("Data sent to DotPad successfully.");
    } catch (error) {
        console.error("Failed to fetch and send data to DotPad:", error);
    }
}
